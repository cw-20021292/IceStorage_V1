U16 IsValidRangeTemp(TEMP_T tRoom, TEMP_T tAmbi )
{
    if( tRoom < 1.0f || tRoom > 45.0f )
    {
        return FALSE;
    }

    if( tAmbi < 0.0f || tAmbi > 43.0f )
    {
        return FALSE;
    }

    return TRUE;
}

/* 정수 및 외기 온도 값은 반올림 한다.
 * 0'C 보다 낮으면 0'C로 취급한다.
 *
 * 정수 온도 및 외기 온도에 따라, 제빙 시간을 반환한다. ( 단위 : 100ms )
 */
U16 GetIcingTime(TEMP_T tRoom, TEMP_T tAmbi )
{
    U16 mu16IdxRoom;
    U16 mu16IdxAmbi;
    U16 mu16IcingTime = 0;

    /* ROUND UP */
    tRoom = tRoom + 0.5f;
    if( tRoom < 1.0f )
    {
        tRoom = 1.0f;
    }

    tAmbi = tAmbi + 0.5f;
    if( tAmbi < 1.0f )
    {
        tAmbi = 0.0f;
    }

    mu16IdxRoom = (U16)tRoom;
    if( mu16IdxRoom >= MAX_INDEX_ROOM_TEMP )
    {
        mu16IdxRoom = MAX_INDEX_ROOM_TEMP - 1 ;
    }

    mu16IdxAmbi = (U16)tAmbi;
    if( mu16IdxAmbi >= MAX_INDEX_AMBI_TEMP )
    {
        mu16IdxAmbi = MAX_INDEX_AMBI_TEMP - 1 ;
    }

    mu16IcingTime = IcingTimeTable[ mu16IdxRoom ][ mu16IdxAmbi ] * 10U;
    return mu16IcingTime;
}

